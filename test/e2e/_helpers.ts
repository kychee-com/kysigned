/**
 * Skip the calling test if the BASE_URL host is unreachable. Lets the suite
 * be authored before the local operator backend is wired up — CI runs the
 * suite against a live URL where reachability is guaranteed.
 */
export async function skipIfUnreachable(t: { skip: (msg?: string) => void }): Promise<void> {
  try {
    const res = await fetch(`${BASE_URL}/v1/health`, {
      headers: ANON_KEY ? { apikey: ANON_KEY } : undefined,
    }).catch(() => null);
    // 404 is fine — server is reachable, just doesn't have /v1/health yet.
    // ECONNREFUSED returns null from .catch(() => null).
    if (!res) {
      t.skip(`BASE_URL ${BASE_URL} unreachable — skipping e2e (set BASE_URL to a live deployment to enable)`);
    }
  } catch {
    t.skip(`BASE_URL ${BASE_URL} unreachable`);
  }
}
